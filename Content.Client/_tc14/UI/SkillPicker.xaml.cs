namespace Content.Client._tc14.UI;

[GenerateTypedNameReferences]
public sealed partial class SkillPicker : BoxContainer
{
    [Dependency] private readonly IConfigurationManager _cfg = default!;
    [Dependency] private readonly IEntityManager _entityManager = default!;
    [Dependency] private readonly IPrototypeManager _prototypeManager = default!;

    private int _priorityPoints = 5;
    private IEnumerable<SkillPrototype> _skills;

    public SkillPicker()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _skills = _prototypeManager.EnumeratePrototypes<SkillPrototype>();
        BuildEntries();
    }

    private void BuildEntries()
    {
        foreach (var skill in _skills)
        {
            var skillNameLabel = new RichTextLabel
            {
                Margin = new Thickness(0, 0, 10, 0),
            };
            skillNameLabel.SetMarkup(Loc.GetString(skill.Name));
            SkillPriorityContainer.AddChild(skillNameLabel);

            var prioritySpinBox = new SpinBox
            {
                HorizontalAlignment = HAlignment.Center,
                HorizontalExpand = true,
                Value = 0,
                IsValid = val => val is >= 0 and <= 3,
            };
            prioritySpinBox.SetButtons([-1], [1]);
            prioritySpinBox.ValueChanged += _ => UpdatePriorities();
            SkillPriorityContainer.AddChild(prioritySpinBox);
        }
    }

